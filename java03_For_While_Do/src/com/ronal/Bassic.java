package com.ronal;

public class Bassic {

    public static void main(String[] args){

        for( int x = 0; x <= 5; x ++){
            System.out.println("Print for ke " + x);
        }
        //============================================
        int i = 0;
        while (i <= 5){
            System.out.println("Print while ke " + i);
            i++;
        }
        //============================================
        do {
            System.out.println("Print do ke " + i);
            i++;
        } while (i <= 10);

    }

}
