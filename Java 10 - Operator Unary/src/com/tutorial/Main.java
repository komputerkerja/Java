package com.tutorial;

public class Main {

    public static void main(String[] args){
        // Unary
        int angka = 1;
        System.out.println(angka);

        int angka2 = 1;
        System.out.println(angka2++);
        System.out.println(angka2++);
        System.out.println(angka2++);
        System.out.println(angka2++);
        System.out.println("==================");
        System.out.println(angka2--);
        System.out.println(angka2--);
        System.out.println(angka2--);
        System.out.println(angka2--);
        System.out.println(angka2--);
    }

}
